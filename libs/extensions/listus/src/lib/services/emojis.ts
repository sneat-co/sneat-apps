export function detectEmoji(s?: string): string | undefined {
	if (s) {
		const words = s.toLowerCase()
			.split(' ');
		for (const word of words) {
			const emoji = emojiByKeyword[word];
			if (emoji) {
				return emoji;
			}
		}
	}
	return undefined;
}
