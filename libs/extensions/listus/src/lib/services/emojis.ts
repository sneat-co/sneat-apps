const emojiByKeyword: Record<string, string> = {};
emojiKeywords
	.filter((item) => item[0] && item[1] && item[1].length)
	.forEach((item) => {
		item[1]
			.filter((kw) => !!kw)
			.forEach((kw) => {
				emojiByKeyword[kw] = item[0];
			});
	});

export function detectEmoji(s?: string): string | undefined {
	if (s) {
		const words = s.toLowerCase().split(' ');
		for (const word of words) {
			const emoji = emojiByKeyword[word];
			if (emoji) {
				return emoji;
			}
		}
	}
	return undefined;
}
